    private void calcPrice() {
        double itemPrice = 0;
        
        //get price of burger (base)
        if (singleRad.isSelected())
        {
            itemPrice = 3.50;
        }
        else 
        {
            itemPrice = 4.75;
        }
        
        //check options add to item price
        if(cheeseCheckbox.isSelected())
            itemPrice = itemPrice += 0.50;
        if(baconCheckbox.isSelected())
            itemPrice = itemPrice += 1.25;
        if(mealCheckbox.isSelected())
            itemPrice = itemPrice += 4.00;
        
        //include the quantity for the item price
        int quantity= 1;
        if (quantityTextField.getText().equals(""))
            quantityTextField.setText("1");
         quantity = Integer.parseInt(quantityTextField.getText());
        itemPrice = itemPrice * quantity;

        
        //show the price no $ symbol
        
        itemPriceTextField.setText(d2.format(itemPrice));
    }
