    private void addOrdermenuBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrdermenuBtnActionPerformed
        //increas total price
        double itemTotal = Double.parseDouble(itemPriceTextField.getText());
        orderTotal += itemTotal;
        
        totalTextField.setText(d2.format(orderTotal));
        //add to order details
        //2 double, cheese, meal at 9.25 each
        int quantity = Integer.parseInt(quantityTextField.getText());
        String type = "";
        if (singleRad.isSelected()) 
            type = "single";
        else 
            type = "double";
        
        String cheese = "", bacon = "", meal = "";
                if (cheeseCheckbox.isSelected())
                    cheese = ", cheese";
                if (baconCheckbox.isSelected())
                    bacon = ", bacon";
                if (mealCheckbox.isSelected())
                    meal = ", meal";
                
                orderTextArea.append(quantity + " " + type + cheese + bacon + meal + " at " + d2.format(itemTotal) + " each\n");
        
    }//GEN-LAST:event_addOrdermenuBtnActionPerformed

